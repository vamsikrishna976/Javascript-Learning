//Comparision Operators
//a == b (True if a and b are the same)
var a = 4;
var b = 4;
document.write(a == b);
console.log(a == b);

//c != d (True if c and d are not the same other-wise prints false)
var c = 4;
var d = 5;
document.write("<br>")
document.write(c != d);
console.log(c != d);

//e > f (True if e greater than f other-wise prints false)
var e = 5;
var f = 4;
document.write("<br>")
document.write(e > f);
console.log(e > f);

//g > h (True if g less than h other-wise prints false)
var g = 6;
var h = 8;
document.write("<br>")
document.write(g < h);
console.log(g < h);

//i >= j (True if i greater than or equal to j other-wise prints false)
var i = 5;
var j = 4;
document.write("<br>")
document.write(i >= j);
console.log(i >= j);

//k <= l (True if k less than or equal to l other-wise prints false)
var k = 34;
var l = 76;
document.write("<br>")
document.write(k <= l);
console.log(k <= l);

