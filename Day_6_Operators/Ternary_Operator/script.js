//Ternary Operator
//The ternary operator is a shorthand way of writing a simple if/else statement. It is
//represented by the ? symbol. The syntax is:
//condition ? expression1 : expression2
var a = 110;
var b = 20;
var result = (a > b) ? true : false;
console.log(result); //true
var result = (a > b) ? a : b;
console.log(result); //110 

var result = (a < b) ? a : b ;
console.log(result); //20
var result = (a < b) ? true : false;
console.log(result); //false

