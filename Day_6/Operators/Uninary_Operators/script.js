

                        //Uninary Operator
//Increment ( ++ )
//Post Increment
var k = 10;
console.log(k++); //10
document.write("<br>");
document.write(k++); //11

var j = 10;
console.log(++j); //11
document.write("<br>");
document.write(++j); //12

//Decrement ( -- )
//Post Decrement
var l = 10;
console.log(l--); //10
document.write("<br>");
document.write(l--); //9

var m = 10;
console.log(--m); //9
document.write("<br>");
document.write(--m); //8
